HUBS = {"TA":"泰安","ZZ":"郑州东","HS":"华山北","DT":"大同南","HX":"衡山西","PDF":"平顶山西"}

def make_transit(entries):
    result = []
    for e in entries:
        train, dep, arr, dur, cls, seat, cost = e[:7]
        sleeper = len(e) >= 8 and e[7]
        result.append({
            "dep": dep, "arr": arr, "cost": float(cost),
            "type": "G" if "高" in cls else ("D" if "动" in cls else ("Z" if "直" in cls else "K")),
            "train": train, "sleeper": sleeper, "seat": seat,
            "source": "12306_real_20260628"
        })
    return result

